# ── UNICODE SANITIZER ──────────────────────────────────────────────────────────
# fpdf2 with built-in fonts only supports Latin-1 (ISO-8859-1).
# This maps every common Unicode character that slips in from vendor APIs,
# UPnP data, or banner grabs to a safe ASCII equivalent.

_UNICODE_MAP = {
    '—': '--',   # em dash        —
    '–': '-',    # en dash        –
    '‒': '-',    # figure dash    ‒
    '‐': '-',    # hyphen         ‐
    '‘': "'",    # left quote     '
    '’': "'",    # right quote    '
    '“': '"',    # left dquote    "
    '”': '"',    # right dquote   "
    '…': '...',  # ellipsis       …
    '·': '*',    # middle dot     ·
    '•': '*',    # bullet         •
    '®': '(R)',  # registered     ®
    '©': '(C)',  # copyright      ©
    '°': 'deg',  # degree         °
    '±': '+/-',  # plus-minus     ±
    '×': 'x',    # multiply       ×
    '→': '->',   # right arrow    →
    '←': '<-',   # left arrow     ←
    '≤': '<=',   # less-equal     ≤
    '≥': '>=',   # greater-equal  ≥
}

def sanitize(text: str, max_len: int = None) -> str:
    """
    Replaces Unicode characters unsupported by Helvetica with ASCII equivalents.
    Drops any remaining non-Latin-1 characters safely.
    Optionally truncates to max_len.
    """
    if not text:
        return ''
    # Apply known replacements
    for uni, asc in _UNICODE_MAP.items():
        text = text.replace(uni, asc)
    # Drop anything still outside Latin-1
    text = text.encode('latin-1', errors='ignore').decode('latin-1')
    if max_len:
        text = text[:max_len]
    return text



# ── CUSTOM PDF CLASS ───────────────────────────────────────────────────────────

class SecurityReport(FPDF):

    def header(self):
        """Prints header on every page except cover."""
        if self.page_no() == 1:
            return

        # Header background
        self.set_fill_color(*COLORS['primary'])
        self.rect(0, 0, 210, 15, 'F')

        # Header text
        self.set_font('Helvetica', 'B', 9)
        self.set_text_color(*COLORS['white'])
        self.set_xy(10, 4)
        self.cell(0, 7, 'IoT Device Security Scanner - Confidential Report', ln=False)

        # Page number right side
        self.set_xy(-40, 4)
        self.cell(30, 7, f'Page {self.page_no()}', align='R')
        self.set_text_color(*COLORS['black'])
        self.ln(15)


    def footer(self):
        """Prints footer on every page."""
        self.set_y(-15)
        self.set_font('Helvetica', 'I', 8)
        self.set_text_color(*COLORS['darkgray'])
        scan_date = datetime.datetime.now().strftime("%Y-%m-%d %H:%M")
        self.cell(0, 10, f'Generated by IoT Security Scanner | {scan_date}', align='C')


    def section_title(self, title):
        """Prints a styled section title."""
        self.set_fill_color(*COLORS['primary'])
        self.set_text_color(*COLORS['white'])
        self.set_font('Helvetica', 'B', 12)
        self.cell(0, 10, f'  {title}', ln=True, fill=True)
        self.set_text_color(*COLORS['black'])
        self.ln(3)


    def risk_color(self, risk):
        """Returns RGB color tuple for a risk level."""
        colors = {
            'CRITICAL': COLORS['critical'],
            'High':     COLORS['high'],
            'Weak':     COLORS['weak'],
            'Safe':     COLORS['safe'],
        }
        return colors.get(risk, COLORS['darkgray'])



# ── SUMMARY PAGE ───────────────────────────────────────────────────────────────

def add_summary_page(pdf, devices, score, score_label, breakdown, trend_img=None):
    """Generates the network summary page."""
    pdf.add_page()
    pdf.section_title('Network Summary')

    # Score overview
    pdf.set_font('Helvetica', 'B', 13)
    pdf.set_text_color(*COLORS['primary'])
    pdf.cell(0, 8, sanitize(f'Overall Network Health Score: {score}/100 ({score_label})'), ln=True)
    pdf.set_text_color(*COLORS['black'])
    pdf.ln(3)

    # Device counts
    critical = sum(1 for d in devices if d['risk'] == 'CRITICAL')
    high     = sum(1 for d in devices if d['risk'] == 'High')
    weak     = sum(1 for d in devices if d['risk'] == 'Weak')
    safe     = sum(1 for d in devices if d['risk'] == 'Safe')

    # Count boxes
    box_data = [
        ('CRITICAL', critical, COLORS['critical']),
        ('HIGH',     high,     COLORS['high']),
        ('WEAK',     weak,     COLORS['weak']),
        ('SAFE',     safe,     COLORS['safe']),
    ]

    x_start = 10
    for label, count, color in box_data:
        pdf.set_fill_color(*color)
        pdf.set_text_color(*COLORS['white'])
        pdf.set_xy(x_start, pdf.get_y())
        pdf.set_font('Helvetica', 'B', 20)
        pdf.cell(42, 18, str(count), align='C', fill=True)
        pdf.set_xy(x_start, pdf.get_y())
        pdf.set_font('Helvetica', '', 8)
        pdf.cell(42, 6, label, align='C', fill=True, ln=False)
        x_start += 48

    pdf.set_text_color(*COLORS['black'])
    pdf.ln(12)

    # Score breakdown table
    pdf.ln(3)
    pdf.section_title('Score Breakdown')

    # Table header
    pdf.set_fill_color(*COLORS['primary'])
    pdf.set_text_color(*COLORS['white'])
    pdf.set_font('Helvetica', 'B', 10)
    pdf.cell(60, 8, 'IP Address',  fill=True, border=1)
    pdf.cell(100, 8, 'Reason',     fill=True, border=1)
    pdf.cell(30, 8, 'Impact',      fill=True, border=1, ln=True)

    # Table rows
    pdf.set_font('Helvetica', '', 9)
    fill = False
    for b in breakdown:
        deduction = b['deduction']
        is_reward = deduction < 0   # negative deduction = bonus points

        pdf.set_fill_color(*COLORS['lightgray'] if fill else COLORS['white'])
        pdf.set_text_color(*COLORS['black'])
        pdf.cell(60,  8, sanitize(b['ip']),     fill=True, border=1)
        pdf.cell(100, 8, sanitize(b['reason']), fill=True, border=1)

        # Rewards show green "+X pts", penalties show red "-X pts"
        if is_reward:
            pdf.set_text_color(*COLORS['safe'])
            pdf.cell(30, 8, f"+{abs(deduction)} pts", fill=True, border=1, ln=True)
        else:
            pdf.set_text_color(*COLORS['critical'])
            pdf.cell(30, 8, f"-{deduction} pts",      fill=True, border=1, ln=True)

        fill = not fill

    # Risk trend graph — embedded below breakdown table if available
    if trend_img and os.path.exists(trend_img):
        pdf.ln(6)
        pdf.section_title('Network Score - Historical Trend')
        pdf.image(trend_img, x=10, w=188)


